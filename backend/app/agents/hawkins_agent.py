import json
from .common import client, settings

def get_hawkins_agent_level(hawkins_score: int) -> int:
    """Determine which level agent to use based on Hawkins score (1-10 system)."""
    if hawkins_score <= 20:   return 1
    if hawkins_score <= 50:   return 2
    if hawkins_score <= 100:  return 3
    if hawkins_score <= 175:  return 4
    if hawkins_score <= 200:  return 5
    if hawkins_score <= 310:  return 6
    if hawkins_score <= 400:  return 7
    if hawkins_score <= 500:  return 8
    if hawkins_score <= 600:  return 9
    return 10

async def evaluate_hawkins(user_text: str, context: str = "") -> dict:
    """
    Evaluate Hawkins score from user's text.
    Returns {score, level, dominant_emotion, confidence}
    """
    prompt = f"""ТЫ — диагностический агент шкалы Хокинса.
Твоя единственная задача: точно определить балл сознания человека на основе ПОЛНОГО контекста сессии.
Ты не помогаешь, не интерпретируешь для человека — ты ставишь диагноз для системы.

═══════════════════════════════════════════
КОНТЕКСТ СЕССИИ
═══════════════════════════════════════════
{context if context else 'не задан'}

═══════════════════════════════════════════
ТЕКСТ ПОЛЬЗОВАТЕЛЯ (последний ввод)
═══════════════════════════════════════════
"{user_text}"

═══════════════════════════════════════════
АЛГОРИТМ ДИАГНОСТИКИ
═══════════════════════════════════════════

ШАГ 1 — ИЗВЛЕЧЕНИЕ СИГНАЛОВ
Прочитай ВЕСЬ контекст сессии и извлеки данные по 5 каналам:

КАНАЛ ТЕЛА: любые упоминания телесных ощущений — сжатие, тепло, холод, тяжесть, 
лёгкость, напряжение, расслабление, "ватность", невесомость. 
Тело — самый достоверный канал. Оно не лжёт.

КАНАЛ ОБРАЗОВ: визуальные описания сцен — свет/темнота, открытость/замкнутость, 
движение/статика, цвета, температура пространства, объекты.

КАНАЛ ДЕЙСТВИЙ: что человек делал в нарративе — стоял/шёл/бежал/прятался/сражался/наблюдал.
Особое внимание: момент выбора под давлением (Разлом/Слой 3) — это чистейший маркер.

КАНАЛ СИМВОЛОВ: ключевой символ сессии и его трансформация:
— degraded (разрушился, потускнел, исчез) → подтверждает нижний диапазон
— static (не изменился) → застой, фиксация
— transformed (раскрылся, засветился, ожил) → потенциал подъёма

КАНАЛ РЕЧИ: паттерны формулировок:
— "не знаю", "всё равно", молчание → апатия (50-100)
— "а вдруг", вопросы о безопасности → страх (100-175)  
— обвинения, "они виноваты", "почему все..." → гнев (175-200)
— "я решу", "моя задача", "интересно" → выше 200
— объяснения, анализ, рационализация → разум (400-500)
— короткие ответы с глубиной, принятие → 500+

ШАГ 2 — ОПРЕДЕЛЕНИЕ НАИНИЗШЕЙ ТОЧКИ
Один маркер страха среди десяти нейтральных = балл в диапазоне страха.
Подсознание прорывается в моментах, а не в фоне.
Ищи: 
— где сменился тон (тепло → холод, движение → замирание)
— где появилось сопротивление (логика, уход от вопроса, "не знаю")
— где тело среагировало раньше разума

ШАГ 3 — ПРОВЕРКА ЧЕРЕЗ РАЗЛОМ
Момент выбора под давлением — самый диагностически чистый.
Приоритет оценки: реакция тела > что выбрал > как объяснил выбор.
— Замер/не выбрал → апатия/страх (50-175)
— Убежал/спрятался → страх (100-175)
— Атаковал/разрушил → гнев (175-200)
— Выбрал осознанно → мужество+ (200+)
— Анализировал вместо действия → разум-защита (400-500)
— Остался спокоен в хаосе → 500+

ШАГ 4 — ПРОВЕРКА НА ФАСАД
Сравни Слой 1 (поверхность) и Слой 4 (дно):
— Слой 1 тёплый/динамичный + Слой 4 холодный/статичный = ФАСАД.
  Реальный балл определяется по Слою 4, не по Слою 1.
— Слой 1 ≈ Слой 4 = консистентность, балл надёжен.

ШАГ 5 — ТОЧНАЯ КАЛИБРОВКА
Используй эту карту для финального определения балла:

0-20:   Полная диссоциация. Нет воли. Паралич. Пустота без потенциала.
20-50:  "Я дефектный". Самообвинение. Разрушение. Стыд блокирует контакт.
50-100: "Всё бесполезно". Ватность. Туман. Нет движения. "Не знаю" как рефлекс.
100-125: Тревога. Сжатие. Закрытые пространства. Постоянная готовность к угрозе.
125-150: Желание/тоска. Ненасытность. Цепляние. "Мне нужно ещё".
150-175: Гнев направленный. Жар. Обвинения. "Они виноваты".
175-200: Гордыня. Презрение. Раздутость. "Я лучше знаю".
200-250: Мужество. Честность с собой. Прямая речь. Первая ответственность.
250-310: Нейтральность. Стабильность. Прагматизм. "Я справлюсь".
310-350: Готовность. Оптимизм. "Я хочу". Активное участие.
350-400: Принятие. Прощение. Гармония. Видит возможности.
400-500: Разум. Анализ. Системность. Риск "жить в голове".
500-540: Любовь безусловная. Присутствие. Служение. Минимум слов — максимум глубины.
540-600: Радость. Покой не зависит от условий. Лёгкость в сложном.
600-700: Мир. Границы "я" растворяются. Единство с бытием.
700-1000: Просветление. Чистое бытие. Нет субъекта и объекта.

КРИТИЧЕСКИЕ ПРАВИЛА:
— 78% людей ниже 200. Не завышай по умолчанию.
— Позитивные декларации ("мне хорошо", "я справляюсь") без телесного подтверждения = возможный фасад.
— Рационализация и объяснения ≠ высокий уровень. Это часто защита на 400-500.
— Молчание и "не знаю" это НЕ нейтральность. Это апатия (50-100).
— Балл ставится по ХУДШЕМУ достоверному маркеру, не по среднему.

ШАГ 6 — ОПРЕДЕЛЕНИЕ УВЕРЕННОСТИ (1-10)
10: все 5 каналов дают согласованные данные
7-9: 3-4 канала согласованы, минорные расхождения
4-6: данные противоречивы, есть фасад
1-3: недостаточно данных для надёжной оценки

═══════════════════════════════════════════
ВЫВОД — СТРОГО JSON, НИЧЕГО КРОМЕ JSON
═══════════════════════════════════════════
{{"score": <число 0-1000>, "level": "<точное название уровня>", "dominant_emotion": "<доминирующая эмоция>", "confidence": <число 1-10>}}
"""

    response = await client.chat.completions.create(
        model=settings.OPENAI_MODEL_FAST,
        messages=[{"role": "user", "content": prompt}],
        max_tokens=150,
        temperature=0.3,
        response_format={"type": "json_object"},
    )

    try:
        return json.loads(response.choices[0].message.content)
    except Exception:
        return {"score": 100, "level": "Страх", "dominant_emotion": "тревога", "confidence": 3}
