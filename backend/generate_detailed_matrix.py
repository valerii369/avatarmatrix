def generate():
    with open(ARCHETYPES_PATH, "r", encoding="utf-8") as f:
        archetypes = json.load(f)
        
    # Read backup to get core shadows and lights
    if os.path.exists(BACKUP_PATH):
        with open(BACKUP_PATH, "r", encoding="utf-8") as f:
            backup = json.load(f)
    else:
        backup = {}

    spheres = ["IDENTITY", "MONEY", "RELATIONS", "FAMILY", "MISSION", "HEALTH", "SOCIETY", "SPIRIT"]
    
    matrix = {}
    
    for arch in archetypes:
        aid = str(arch["id"])
        matrix[aid] = {
            "_meta": {
                "archetype_id": arch["id"],
                "archetype_name": arch["name"],
                "archetype_name_en": arch["name_en"]
            }
        }
        
        flavor = ARCHETYPE_FLAVORS.get(arch["name"], [])
        
        for sphere in spheres:
            core_shadow = backup.get(aid, {}).get(sphere, {}).get("shadow", arch.get("shadow", ""))
            core_light = backup.get(aid, {}).get(sphere, {}).get("light", arch.get("light", ""))
            core_desc = backup.get(aid, {}).get(sphere, {}).get("description", arch.get("description", ""))
            
            levels_dict = {}
            for level in HAWKINS_LEVELS:
                # Merge base markers with flavor
                markers = list(set(level["speech_markers"] + flavor[:2]))
                
                levels_dict[level["score"]] = {
                    "level_name": level["name"],
                    "consciousness": level["consciousness"].format(arch=arch["name"].upper(), sphere=sphere),
                    "thinking": level["thinking"],
                    "body": level["body"],
                    "relations": level["relations"],
                    "speech": level["speech"],
                    "quantum_shift": level["quantum_shift"].format(arch=arch["name"].upper(), sphere=sphere),
                    "speech_markers": markers
                }
            
            matrix[aid][sphere] = {
                "shadow": core_shadow,
                "light": core_light,
                "description": core_desc,
                "core_shadow": core_shadow,
                "core_light": core_light,
                "core_description": core_desc,
                "levels": levels_dict,
                "agent_question": f"Где именно сейчас энергия {arch['name']} в сфере {sphere}?",
                "linked_cards": [],
                "xp_reward": 50
            }
            
    with open(MATRIX_PATH, "w", encoding="utf-8") as f:
        json.dump(matrix, f, ensure_ascii=False, indent=2)
        
    print(f"Detailed Matrix Generation Complete. Total archetypes: {len(matrix)}")

if __name__ == "__main__":
    generate()
